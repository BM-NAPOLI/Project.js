let day =  Math.floor(Math.random() * 7);

switch (day) {
    case 0:
        document.write("Lundi")
        break;
    case 1:
        document.write("Mardi")
        break;
    case 2:
        document.write("Mercredi")
        break;
    case 3:
        document.write("Jeudi")
        break;
    case 4:
        document.write("Vendredi")
        break;
    case 5:
        document.write("Samdi")
        break;
    case 6:
        document.write("Dimanch")
        break;

    default: document.write("Endifand Day")
        break;
}